package com.classdesign.domain;

public class Movie {
    private int movie_id;
    private String movie_name;
    private String movie_another_name;
    private String movie_type;
    private String movie_language;
    private String movie_date;
    private String movie_location;
    private String movie_length;
    private String movie_introduce;
    private String movie_pic_context;

    public int getMovie_id() {
        return movie_id;
    }

    public void setMovie_id(int movie_id) {
        this.movie_id = movie_id;
    }

    public String getMovie_name() {
        return movie_name;
    }

    public void setMovie_name(String movie_name) {
        this.movie_name = movie_name;
    }

    public String getMovie_another_name() {
        return movie_another_name;
    }

    public void setMovie_another_name(String movie_another_name) {
        this.movie_another_name = movie_another_name;
    }

    public String getMovie_type() {
        return movie_type;
    }

    public void setMovie_type(String movie_type) {
        this.movie_type = movie_type;
    }

    public String getMovie_language() {
        return movie_language;
    }

    public void setMovie_language(String movie_language) {
        this.movie_language = movie_language;
    }

    public String getMovie_date() {
        return movie_date;
    }

    public void setMovie_date(String movie_date) {
        this.movie_date = movie_date;
    }

    public String getMovie_location() {
        return movie_location;
    }

    public void setMovie_location(String movie_location) {
        this.movie_location = movie_location;
    }

    public String getMovie_length() {
        return movie_length;
    }

    public void setMovie_length(String movie_length) {
        this.movie_length = movie_length;
    }

    public String getMovie_introduce() {
        return movie_introduce;
    }

    public void setMovie_introduce(String movie_introduce) {
        this.movie_introduce = movie_introduce;
    }

    public String getMovie_pic_context() {
        return movie_pic_context;
    }

    public void setMovie_pic_context(String movie_pic_context) {
        this.movie_pic_context = movie_pic_context;
    }
}
